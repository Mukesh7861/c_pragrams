//2. Write a program to make Simple calculator (to make addition , subtraction , multiplication , division and modulo)
#include<stdio.h>
int main(){
	int a,b;
	printf("Enter the first value 1: ");
	scanf("%d",&a);
	printf("second value 2: ");
	scanf("%d",&b);
	
	printf("addition is : %d\n",a+b);
	printf("subtracion is : %d\n",a-b);
	printf("multiplication is : %d\n",a*b);
	printf("division is : %d\n",a/b);
	printf("modulus is : %d\n",a%b);
	
	
	
	
	return 0;
}
